<P>There is an old saying that says "if you can read you can cook." That's only 
true after you've had a little experience! <A href="../glossary/index.php">Cooking terms</A> can be confusing, and most cookbooks aren't as detailed as many beginning cooks would like them to be. 
<P>If cooking is a science, then a recipe is like a chemical formula. Once you learn the language, your cooking expertise and knowledge will grow by leaps and bounds. The words in <B>bold</B> are explained below. <FONT color="#800000">
<P><h3>Simple Spaghetti </h3>
<P>1 <B>Tbsp.</B> olive oil
<BR>1 cup <B>chopped onion</B>
<BR>2 cloves garlic, minced
<BR>1/2 pound ground beef
<BR>2 8-oz. cans tomato sauce
<BR>1-1/2 cups 
water
<BR>1 <B>tsp.</B> salt
<BR>1 tsp. dried parsley
<BR>1 tsp. dried basil<BR>1/4 
tsp. pepper
<BR>4 <B>oz.</B> uncooked spaghetti pasta, broken in half<BR>

<P><B>Heat</B> olive oil in heavy skillet over <B>medium heat</B> and add onion 
and garlic. Cook and stir until <B>translucent</B>. Add ground beef and cook and 
stir until beef is <B>browned</B> and vegetables are <B>tender</B>. Stir in 
remaining ingredients except for uncooked spaghetti. Bring to a boil, reduce 
heat, and <B>simmer</B> for 3 minutes. 
<P>Add uncooked spaghetti to the simmering sauce a little at a time, stirring to 
keep it separated. Cover tightly and simmer for 20-25 minutes over <B>low 
heat</B> or until <B>pasta is tender</B>, <B>stirring frequently</B>. Serve with 
grated Parmesan cheese. Serves 3-4</FONT> 

<UL>
  <LI>The very first step in cooking is to read the recipe all the way through, from beginning to end. This way you will know that you have all the ingredients and tools in the recipe available for use. You will also be able to look up terms you don't fully understand so cooking proceeds smoothly. 
  <LI>Most good recipes start with the ingredient list, and the ingredients are listed in the order they are used. In this case, the olive oil goes in the pan first, followed by the onions and the garlic. 
  <LI>Measurements in recipes are critical. When a recipe calls for a tablespoon or teaspoon, the author means for you to use actual measuring utensils, not spoons that you use for eating and serving. Here's a basic <A href="refchart.php">chart</A> of measures and equivalents that may be helpful in the kitchen. For our purposes, these are the abbreviations I use that are fairly standard:
  <b><UL>
    <LI>Tbsp. (T) = Tablespoon 
    <LI>tsp. (t) = teaspoon 
    <LI>oz. = ounce</LI></UL></b>
  <LI>Even the order of words in an ingredient list changes the preparation of the foods. For instance, if a recipe calls for "1 cup nuts, chopped", that is different from "1 cup chopped nuts". In the first case, you should measure 1 cup of unchopped shelled nuts first, then chop them. In the second case, the nuts should be chopped first, then measured. The comma placement changes the measuring technique. 
  <LI>In the recipe above, the onions are chopped and then measured. 
  <LI>After you have read the recipe, gather all the ingredients, pots, pans, bowls, and measuring utensils you will need. Go slowly and double check all the steps and ingredients. 
  <LI>The body of the recipe contains the instructions about combining and heating the ingredients. In the spaghetti recipe above: 
  <UL>

    <LI><B>Heating</B> the olive oil means place it in a skillet, turning on the heat to medium, and leaving the oil on the heat for 1-2 minutes, until you can feel the warmth when you hold your hand 3-4 inches above the pan. 
    <LI>The degrees of heat are usually marked on your oven dials. I always turn the dial so it points to the lowest part of the heat setting. <u>You can always turn the heat up, but overcooking food is permanent!</u> <B>Medium heat</B> is right in the middle of the dial. <B>Low heat</B> is also marked, and is the bottom 1/4 in the range from off to high. 
    <LI>Cooking the onions until <B>translucent</B> means the color of the onions changes from pure white to a softer white that is more transparent or brownish in color. 
    <LI><B>Browning</B> the ground beef means to cook just until the pink or red color disappears. Stir with a fork so the chunk of ground beef breaks up as it cooks and you are left with small uniform pieces. This does not mean to cook until the meat turns the color of dark woodwork. 
    <LI>Cooking vegetables until <B>tender</B> means that when you poke or pierce them with a fork, the tines of the fork slide easily into the flesh, with little resistance. 
    <LI>Simmering and boiling are degrees of cooking. A <B>simmer</B> means small bubbles rise to the surface of the liquid slowly. Simmering liquid doesn't make much noise. Boiling means large bubbles rise to the surface of the liquid quickly. Boiling liquid is quite noisy. 
    <LI><B>Pasta is tender</B> when it is cooked all the way through. To test that, remove one strand of pasta from the sauce, rinse it with cool water 
    and carefully cut it in half. There should be no white areas inside the pasta, or only a thin white line if you like your pasta to have more texture. Then taste it. The pasta should not taste of flour, and the texture should be tender but still firm. 
    <LI><B>Stirring frequently</B> means to manipulate the ingredients with a spoon every 2-3 minutes.  In this case, stirring is to insure the pasta will not stick to the pot, and so it gets evenly cooked through. 
    <LI>All recipes have a cooking time range. These times are tested using tolerance techniques in test kitchens. Begin testing for doneness at the beginning of the time range. In the recipe above, start checking the tenderness of the spaghetti at 20 minutes. You shouldn't have to cook the dish beyond 25 minutes, although many factors can influence timing. Just remember to start testing at the beginning of the cooking range, and remove the food from the heat when it tastes good to you! </LI></UL>

  <P>I hope this explanation of a simple pasta recipe will help in your cooking adventures! 


