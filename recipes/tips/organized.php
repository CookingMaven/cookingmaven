<p>Have you ever felt overwhelmed with a recipe and all the ingredients that go into it?  What about getting halfway through a recipe only to find out you only have 3 eggs and it calls for 4?  Staying organized in the kitchen is essential to all cooks, and following these steps can ensure that you all are able to keep organized!

<p>A great way to start cooking each dish is by taking all of the necessary ingredients out prior to starting to cook.  One thing that some people don’t do, though, is remember what they have already done for the recipe.  One way to prevent this from happening to you, is after you use an ingredient just put it away, or put it out of the way.  This can be helpful so there is no blank stare into your bowl of ingredients asking, “Did I already add the oil?”

<p>Keeping track of which ingredients you use is just part of the challenge.  Being able to keep track of how much of an ingredient has already been added, especially with baking, can be helped by using some coins.  When the recipe calls for 8 cups of flour, 5 cups of sugar, 3 tbsp of baking powder, and 4 tsp of salt, any mind can get jumbled with all the numbers.  The way this tip will help, is when you add a cup (or tsp, or oz, or anything) to your mixing bowl then you just put one of your coins to the side.  When the right amount of coins are set aside then you have the right amount in your dish.

<p>A final tip to staying organized is to keep a trash bag/bowl close by so you are keeping your work area clean as you cook.  Making sure that your prep area is staying clean will ensure that your food tastes the best and you are not accidentally getting peels or other items mixed with your good ingredients.

