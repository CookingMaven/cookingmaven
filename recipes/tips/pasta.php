<p> Everyone loves a good pasta dish.  Cooking pasta, however, can be finicky at best if you aren't careful.  These 5 tips will help make sure your pasta doesn't become sticky or mushy and will turn out perfect every time!

<p><b>1: Don't put oil in the water</b> - Seasoning the water prior to cooking with salt or some other items to help flavor the pasta can be a big taste improver.  The problem with putting oil in the water is that it makes your dish instantly less healthy and greasy.  People think that this will keep your noodles from sticking, but this is just a myth and cooking your noodles properly will do that for you!

<p><b>2: Make sure the pot is the right size</b> - People make this mistake far too often, by either trying to break the noodles to get them to fit into a small pot, or having far too much water in a pot and having it take forever to boil.

<br>By using a pot that's not large enough, the water temperature drops significantly when the pasta is added, more so than if you were using a larger pot. While the water returns to a boil (which can take a while if the pot is very large), the pasta gets clumpy, sticky, or mushy sitting in the pot. As I mentioned above, using oil to cook your pasta wont prevent it from being sticky and all it takes is proper cooking for it to turn out perfect!  

<p><b>3: Make sure the water is hot enough</b> - There are two reasons for following this tip.  First, if the water isn't at the right temperature you could be under cooking your noodles if a few of the minutes are not at the right temperature.  Second, when pasta sits in water that isn't hot enough it can become mushy and no one likes that.  As you add pasta to the water, it will bring the water temperature down already, so be sure to give it enough time to properly heat up before adding your pasta!

<p><b>4: Stir your pasta</b> - As we saw with a lot of the other tips, a major issue with cooking pasta is having the noodles get mushy or stick together.  Stirring your noodles for the first few minutes that they are in the hot water will go a long ways in ensuring that the noodles don't stick together.  I like to stir it for the first 15 to 30 seconds, and then come back every few minutes and give it a few quick stirs!

<p><b>5: Don't rinse your pasta</b> - This is one of those tips that some people always fail to obey, and I am sure their pasta dishes turn out bland.  When cooking, there are lots of great starches and flavors from the cooking.  Typically when cooking a dish, I will leave a little bit of the water with the pasta when putting it with the sauce for the last few minutes in order to get all of that flavor into the dish.  Using a basic colander to drain your pasta will work great, and help ensure your dish tastes as great as possible!

<p> Hopefully these tips help make your next pasta dish a great success!


