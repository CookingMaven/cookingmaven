<p>If you have ever been frustrated cutting individual grapes or tomatoes for your salads or kids meals, this is the tip for you.  If you follow the 3 easy steps below, you will be able to cut grapes perfectly for your next Chicken Salad sandwich every time.

<ul>
<li>Get 2 equally sized plates that are relatively flat</li>
<li>Place your grapes or tomatoes on the top of one of the plates, and put the other plate on top to hold them flat</li>
<li>Get a long, sharp knife, and while gently pressing on the top plate to keep things in place, cut through the fruit</li>
</ul>

