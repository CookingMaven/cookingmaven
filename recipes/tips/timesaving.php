<p><b>1: Make Extra</b> - One of the easiest time saving tips in the kitchen is to throw in a little extra and have leftovers for the next day.  This will allow you to spend a few minutes today to make your meal tomorrow go very quickly.  Whether it is an extra cup of rice, 1 more chicken breast on the grill, or chopping some extra vegetables; this pre work will save you tons of time the next day.

<p><b>2: Shop Smart</b> - To save even more time when cooking, make sure that you won't have to run to the grocery store in the middle of your prep work to buy an onion or some other ingredient that you might have missed.  Make a shopping list, or have the recipe that you will be making handy while at the store and you will hopefully be able to make only 1 trip for you meal!

<p><b>3: Have the Right Equipment</b> - This tip is pretty straight forward, but if your recipe calls for whisking something, it's a lot easier to do so with a whisk as opposed to a fork or something else.  Understanding how to prepare the meal for that night ahead of time will let you ensure that you are prepared.

<p><b>4: Stay Organized</b> - As I discussed in more detail <a href="organized.php">here</a>, staying organized in the kitchen will keep you from doing things twice or doing them wrong, hence saving you all sorts of potential time.

<p><b>5: Pre-made Items Can Help</b> - This doesn't mean buying a frozen TV Dinner, but it does mean that pre made sauces or pre cut fruits/vegetables when you know you will be tight on time can be a huge time saver.  A little seasoning and the right recipe and this time saver will still produce a great homemade meal!

