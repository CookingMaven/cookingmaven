<p> Whether you are traveling for work or on vacation with friends or family, there are a few things that you can do to ensure that you are eating well.  Some people travel every week, and others only every so often, either way shouldn't change your ability to eat well.  I get to travel a lot for work, and the items below are what I try and do whenever possible!

<ol><li><b>Limit snacking</b>.  Everyone loves to have candy, Pringles, and a little extra caffeine while on the road. Rather then the typical junk food, when you do need to have a snack, try some trail mix or fruit and veggie slices to tide you over until you get to your destination.

<li><b>Go to a grocery store</b>.  When you get to your final destination, stop at a grocery store to pick up some fresh foods.  Not only will this allow you to save money from eating out every meal on your trip, but you can have more control over what you are eating.

<li><b>Stay hydrated</b>.  When traveling, especially on a long flight, you need to make a point to drink a lot of water to avoid the side effects of dehydration, on top of any jet lag or other travel side effects. 

<li><b>Be prepared</b>.  There are a lot of ways that you can be prepared for your trip.  Try bringing a few tupperware containers and ziplock baggies with you to keep any food you do buy fresh.  This will save you from throwing away half of your food midway through your trip!

<li><b>Try the local cuisine</b>!  When I am traveling and I do want to go out for a meal, I always prefer to ask locals what the best local food might be, as opposed to going to the places that I might have heard of.  After a long day of working the easiest thing to do is to pick up carry out on my way to the hotel, but taking a little extra time to get some quality food is always the better option!
</ol>
