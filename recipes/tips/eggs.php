<p>Often times people have real issues with getting hard boiled eggs done correctly.  I have seen runny yolks inside, rubbery eggs, or broken eggs in the pot.  All of these could be avoided by following one of these 2 methods for getting the perfect egg!
<img src ="/images/eggs.jpg" width="200" height="200" hspace="20" vspace="30" align="left">
<p>
<ol>
<li>Carefully place your eggs into the bottom of a saucepan, and fill cold water to about 2 inches above the level of the eggs.  Add some salt to the water, and place over heat until the water boils.  Once your water is boiling, turn off the heat, cover your pot, and let it cook for 13 minutes.  Once your time is up, place the eggs in an ice-bath and let cool for a few minutes.  Then, peel and enjoy!
<li>In a muffin tin, place 1 egg carefully into each of the containers.  Then, place the muffin tin into a 350 degree F oven for 30 minutes.  Once baked, remove from the pan, and place them into an ice-bath to let them cool, peel, and enjoy!
</ol>

<p>The keys to both methods are to be careful with your eggs, cook them for the proper amount of time, and do whichever method you are most comfortable with!  Hope you are able to enjoy hard boiled eggs now, and let me know which way is your preferred method on <a href="http://www.twitter.com/thecookingmaven" target="new">Twitter</a> or <a href="http://www.instagram.com/theDisneyMaven" target="new">Instagram</a>!

