<p><b>1. Let it Be</b> - When grilling, the biggest problem for most new grillers stems from impatience.  Whether this is "overflipping" the food, or just taking it off before it has fully cooked, grilling takes time.  Also, every time you open the lid of the grill, the heat will leave and make it take longer to fully cook through.

<p><img src="/images/grilling.jpg" hspace="15" align="left" height="325" width="280">A lot of times I hear people talk about how the food is sticking to the grates of the grill when they try to flip it.  This is because they are trying to move the food before it's seared on the bottom, and ready to be flipped.  Patience will be the best way to avoid this from happening and will lead to those perfect grill marks you see on TV.

<p><b>2. Don't Use a Fork</b> - Using a fork to flip your meat will puncture it and inevitably allow for the juices to leave the meat.  This will end up making your food dry, or tasteless.  Find a good pair of tongs or a spatula and try that instead!

<p><b>3. Let it Be; Again</b> - Just like in tip 1 with letting the meat be while on the grill, the same can be said for when you take the meat off of the grill.  Letting your meat rest for around 3 minutes before cutting it will allow the juices to redistribute and thicken in order to stay in your piece of meat, and not have it end up on your bun or plate. This will also prevent your meat from drying out.


