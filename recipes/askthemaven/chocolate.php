<b>Question?</b> I have always wanted to make my own chocolate covered strawberries at home, but the chocolate always burns.  What am I doing wrong?

<p><b>Answer:</b> The first, and most important, part of this question is getting the melted chocolate correct.  There are many different ways to melt chocolate. For any of those methods, the first step is to chop the chocolate into small pieces to allow for more even heating. A serrated knife is a good way to do this. 

<p>One, slower, method for the actual melting starts by placing the small pieces of chocolate into a heatproof bowl over a pan of barely simmering water, or the top part of a double boiler if you have one. The bowl should be wide enough that any steam escaping from the pan will not enter the bowl. In addition, the bowl itself should not touch the water. This is because excessive heat can burn it, and any heat reaching the chocolate can adversely effect the flavor. In addition, contact with water can cause chocolate to seize up and become lumpy. 

<p>After the chocolate just begins to melt, remove the bowl from the heat and stir the chocolate to prevent it from overheating. Milk and white chocolate must be stirred frequently while melting because they contain milk solids that lump if unstirred. 

<p><img src="/images/strawberry.jpg" align="left" height="190" width="165">Another more simple way to melt chocolate is by using a microwave safe container to hold your chocolate (I use a measuring cup) and put it in the microwave on high power.  I typically will heat the chocolate for around 45 seconds, stirring the chocolate every 15 to 20 seconds after this until it is fully melted.  Be careful, as you can very easily over cook the chocolate in the microwave.

<p>After the chocolate is melted, take some wax paper and cleaned strawberries, and dip them into the bowl of melted chocolate.  The chocolate will harden fairly quick, so be prepared to start the dipping right away, and be sure to keep stirring the chocolate as you are working.  After cooling the strawberries for about 15 minutes, you will have a great dessert! 

<p>As you get more comfortable with melting chocolate, try melting two different types of chocolate (I like to use white chocolate) in separate containers and drizzle some of the second chocolate on top of your strawberry after it cools.  This will look even fancier and will be a guaranteed hit!
