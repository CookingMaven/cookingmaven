<b>Question?</b> I have heard a lot about saurkraut, but what exactly is it?

<p><b>Answer:</b> Sauerkraut, as defined in the <a href="/glossary/3.php">glossary</a>, is German for shredded and pickled cabbage.  Typically you will find this on top of brats and Rueben's, as well as a topping for other dishes.

<p>Sauerkraut is something that can be made at home if you have cabbage, some salt and pepper, and a sealable container.  When in the jar, the cabbage will release its own brining liquids and naturally ferment.  After several days or a week, it will turn sour and crunchy and become sauerkraut!


