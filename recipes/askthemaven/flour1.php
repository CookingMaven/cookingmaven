<b>Question?</b> My recipe calls for cake flour, but I don't have any of that...what can I do?

<p><b>Answer:</b> Sometimes when baking your recipe might call for 1 cup of cake flour.  While most people might not have cake flour on hand, you are able to a substitute 7/8 cup of all-purpose flour in your recipe.  Cake flour is a very similar ingredient to all purpose flour, with the main difference being that cake flour will have less gluten.  

<p>If someone in your family prefers to eat with less gluten in their diet, feel free to substitute the other way, with 1 cup of cake flour for every 7/8 of a cup of all purpose flour that your recipe might call for.

