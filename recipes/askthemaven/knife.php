<b>Question?</b> What should I be looking for when buying a new set of knives?

<p><b>Answer:</b> The biggest part of finding the right knife set is finding the knives that are the most comfortable to you.  What fits well for one person, very easily might not be the best bet for you.

<p><img src="/images/knife.jpg" hspace="20" align="left" width="225" height="225">For me, I was able to go to a home show that had a number of vendors present. This included Cutco and Hammer Stahl, among others.  At these booths, they have all of their knife options as well as food items to cut to ensure that you like the feeling of the knives.  I can't stress enough that you must try the knives in person before purchasing a quality set of knives because each knife is unique.  Between the length of knives, quality, weighting, and handle grips, each person will be able to find the brand that is right for them.

<p>I ended up choosing to go with the <a href="http://www.hammerstahl.com/">Hammer Stahl</a> knives for my personal kitchen.  Since I bought at a home show, the sales rep threw in some extras including a free block for storing the knives and an extra pairing knife to fill in the gap in my set.  These sales reps are always friendly and are very knowledgeable about their products and want to see you get the right knife for what you want to do. 


