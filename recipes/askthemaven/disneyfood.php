<b>Question?</b> What are your favorite foods to eat while at the Walt Disney World resort in Orlando, Florida?

<p><b>Answer:</b> Disney World has so many amazing food choices all across the property, it really is tough to narrow it down to my favorites.  Below, I highlight my current top 5 favorite dishes from my trip to Disney World in November of 2016.  There are many other dishes that didn't make the cut, but be sure to try them out as I am sure you will find your own favorites somewhere on property!
<p>
<img src ="/images/disneyfood.jpg" width="300" height="300" hspace="20" vspace="30" align="left">
<p>
<li><b>Sweet and Spicy Chicken and Waffle</b> - Found at Sleepy Hollow in the Magic Kingdom - Breaded chicken in a slightly spicy sauce on top of a fresh cooked waffle, served with cole slaw on top</ul>
<li><b>Morimoto Baby Back Ribs</b> - Found at Morimoto Street Food in Disney Springs - Pork Ribs, Cilantro and Hoisin Sweet Chili Glaze cooked to "fall off the bone" perfection</ul>
<li><b>Dole Whip</b> - Found at Aloha Isle in the Magic Kingdom - One of my all time favorite snacks, this Pineapple non-dairy frozen dessert is a real family favorite!  To make this at home, check out my recipe <a href="/recipe/44.php">here</a>.</ul>
<li><b>The Maelstrom Margarita</b> - Found at Cava Del Tequila in the Mexico pavilion at EPCOT - Whether drinking around the world, or relaxing with a drink and some chips and salsa, the Maelstrom margarita is a great selection as a blend of tequila, mango puree, orange liquor, habanero peppers, blueberries, basil, and a tajin chili powder on the rim! </ul>
<li><b>The Grey Stuff</b> - Found at Be Our Guest Restaurant in the Magic Kingdom - Of fame from the movie Beauty and the Beast, the Grey Stuff is a chocolate cupcake with special grey frosting!  A must try, because it's delicious and if you don't believe me, just ask the dishes!</ul>
</li>

