<b>Question?</b> How do I make a stock from scratch?

<p><b>Answer:</b> Everyone probably has a few pre-made bouillon cubes hiding out in the back of our cupboard, but homemade stocks, especially meat stocks, take some love to perfect. The remains of a roast make a great starting point for your stock, whereas stock from raw bones, trimmings or giblets will have a bigger flavor profile.

<b><p>Basic Ingredients</b>
<br>For a versatile stock, the basics ingredients are water, bones from your favorite meat, trimmings or giblets from your favorite meat, celery, onions, carrots and a few herbs such as parsley stalks, a bay leaf or thyme sprigs (or ideally all three tied in a bundle), plus six or so peppercorns. <b>Tip:</b> <i>Stock isn't salted, to avoid over-seasoning your finished dish.</i>

<p>Meat stocks can take a few hours, so big batches are most economical. Save bones and trimmings in the freezer, or buy cheaply from the butchers.

<p>I don't have an exact recipe for a stock, only ensuring that you add enough water to cover the main ingredients (1.5kg bones will make roughly 1 liter of unreduced stock), and following these four key steps:

<ul><li><b>Boil then skim:</b> All stock is boiled once to begin with, bringing froth to the surface of meat and fish stocks. Sweep a large spoon across the pan to remove as much as you can.  This will help the stock not get too greasy as well as keep the flavor profile in tact.

<li><b>Getting the flavor takes time:</b>A good, clear stock is simmered slowly and gently. Once it boils, reduce the heat until bubbles bob lazily to the surface, then cover the pan. It won't need much attention, except a quick stir now and again. A slow cooker is perfect for this, and can be left all day to simmer, if you can wait that long.

<li><b>Strain it:</b> Lift the veg and bones from the stock, then pass it through a sieve and cool completely. Scoop away any fat.

<li><b>Reduce and store:</b> To boost flavor and to save space, boil strained stock down by half (you can always add water back again). Chill and use within 3 days, or freeze to save for another day.
</ul>
