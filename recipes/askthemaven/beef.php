<b>Question?</b> What's the difference between the different cuts of beef at the grocery store?
<p>
<IMG SRC="../images/beef_diagram.gif" height="250" width="315" align="left"><p><b>Answer:</b> There are a lot of different cuts of beef that you might have heard of when shopping or at a restaurant.  To the left is an image of where these cuts of beef come from on the cow.

<p> When picking the right cut of meet at the butcher, there are a lot of items to think about to make sure you are getting the cut that is right for what you are trying to do.
<p>A quick explanation of each cut of beef is below, and it will be best to try each and get a feel for what you are wanting to eat.  It also depends on how you are planning to prepare the meat as to which cut is best for you.

</font></td></tr></table>
<p>
<h4><font color="#800000">Forequarter cuts</font></h4>
<ul>
<FONT FACE="ARIAL,HELVETICA" SIZE="2">
<li>The <b>chuck</b> is the source of bone-in chuck steaks and roasts, and boneless clod steaks and roasts. The trimmings and some whole boneless chucks are ground for hamburgers.
<li>The <b>rib</b> contains part of the short ribs, found in the prime rib and rib eye steaks you eat today.
<li><b>Brisket</b>, is primarily used for barbecue, corned beef or pastrami.
<li>The <b>foreshank or shank</b> is used primarily for stews and soups; it is not usually served any other way because it is the toughest of the cuts and requires long amounts of slow cooking to become flavorful.
<li>The <b>short plate</b> is the other source of short ribs, used for pot roasting, and the outside skirt steak, which is commonly used for fajitas. The remainder is usually ground, as it is typically a cheap, tough, and fatty meat.
</ul>
</font>
<p>

<h4><font color="#800000">Hindquarter cuts</font></h4>
<ul>
<FONT FACE="ARIAL,HELVETICA" SIZE="2">
<li>The <b>loin</b> has two components, or three if boneless:
<ul>
<li>the <b>short loin</b>, from which the T-bone and Porterhouse steaks are cut if bone-in, or strip steak (New York Strip if served without the bone, and Kansas City strip if bone in)
<li>the <b>sirloin</b>, which is less tender than the short loin but more flavorful, can be further divided into top sirloin and bottom sirloin (including tri-tip), and
<li>the <b>tenderloin</b>, which is the most tender, can be removed as a separate cut, and cut into filet mignon, tenderloin steaks, and roasts (such as for beef Wellington). They can also be cut bone-in to make parts of the T-bone and Porterhouse steaks.
</ul>
<li>The <b>round</b> contains lean, moderately tough, lower fat cuts, which usually require rare cooking. Some common cuts are round steak, eye of round, top round, and bottom round steaks and roasts.
<li>The <b>flank</b> is used mostly for grinding, except for the long and flat flank steak, best known for use in London broil, and the inside skirt steak, also used for fajitas. Flank steaks were once one of the most affordable steaks, because they are substantially tougher than the more desirable loin and rib steaks. Many modern recipes for flank steak use marinades or moist cooking methods, such as braising, to improve the tenderness and flavor.
</ul>
