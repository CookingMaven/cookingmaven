<b>Question?</b> What are the differences between all of the types of sausages at the store?

<p><b>Answer:</b> When buying sausage, you have a number of items to consider.  There are 5 main types of sausage, but within that, sometimes there are different flavors that are available as well.  The main 5 types of sausages are:


<ul><li><b>Kielbasa:</b> "Kielbasa" is the Polish word for sausage and in Poland it can mean any kind of sausage. In the U.S., kielbasa (also known simply as "Polish sausage") refers to a horseshoe-shaped, pork country sausage that is most often smoked, but is sometimes partially smoked or unsmoked.

<p><i>How to prepare Kielbasa</i>: This type of sausage is great on the grill or sauteed and served over your favorite Central European sides; boiled potatoes, braised cabbage or sauerkraut. Kielbasa also works well on a bun with mustard or sliced and served in soup. Be sure to cook this all the way through, as it tastes best served warm!

<li><b>Andouille:</b> Andouille is a spicy smoked sausage originally from France, but known mainly for its role in Cajun cuisine, where it is a key ingredient in jambalaya and gumbo. You can also use it in any recipe that calls for smoked sausage if you want a to make it a little bit spicy! 

<p><i>How to prepare Andouille:</i> Because it's smoked, Andouille is precooked and can be sliced and served cold as a snack or appetizer. When you're cooking with it, simply slice and add to your dish; for added flavor, give the Andouille a quick toss in a hot pan to brown the edges.

<li><b>Bratwurst:</b> Bratwurst are a German sausage, where there are more than a dozen varieties. In the U.S., however, it's typically made of pork and veal, and seasoned with salt, ginger, nutmeg and caraway.

<p><i>How to prepare Bratwurst:</i> Bratwurst (or brats) are usually grilled or sauteed.

<li><b>Chorizo:</b> In the U.S., chorizo usually refers to the Mexican variety of sausage, which is uncured and flavored with chilies, garlic and spices, although the exact ingredients can vary.

<p><i>How to prepare Chorizo:</i> Most often sold in casings, chorizo is generally removed from them and cooked prior to use. Simply saute it as you would ground beef and use it in enchiladas, tacos, burritos, soups or stews.


<li><b>Italian:</b> Italian sausage comes in two varieties - hot and sweet. Hot sausage is typically made from pork and seasoned with salt, garlic, anise seed and red pepper flakes. Sweet sausage omits the pepper flakes but is otherwise the same. These types of sausage are interchangeable; it just depends on your spice tolerance. 

<p><i>How to Prepare Italian Sausage:</i> Italian sausage can be used in a variety of ways. Grill or saute the Italian sausage as you would a bratwurst and serve on a roll with onions and peppers.  You can also serve minced, sauteed sausage on top of a pizza. Italian sausage can also be added to tomato sauce - either minced and sauteed or removed from its casing altogether - and served over pasta.

</ul>
