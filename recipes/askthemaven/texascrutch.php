<b>Question?</b> I have been smoking meats lately, and it reaches a good temperature after a few hours but stops warming.  Am I doing something wrong, or is there a way to stop this from happening?

<p><b>Answer:</b> In barbeque cooking, specifically smoking, the meat will reach a temperature (between 150 and 165 degrees) where it will stop warming any further known as <a href="/glossary/114.php">the stall.</a>  To combat the stall, people will wrap their ribs, brisket, or pork in aluminum foil to help with the heating process.  This is affectionately known as the Texas Crutch as it was created in the Texas style bbq.  Adding a little bit of liquid into the foil, and putting it back on the smoker will allow the meat to braise and heat faster and more evenly to get over the stall point and reach the ideal internal temperature for the meat.

<p>Some people also use butcher paper or covered pans to replicate this process and ensure their meat is cooked properly.  The biggest thing to remember in this process is that the meat has to reach the proper internal temperature of around 203 degrees for Pork and Brisket.  However you decide to get the meat there is up to you, but the Texas Crutch method will help you get over the stall faster than leaving it unwrapped.

