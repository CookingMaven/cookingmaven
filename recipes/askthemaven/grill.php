<b>Question?</b> What should I be looking for when buying a new grill?

<p><b>Answer:</b> One of my favorite ways to cook is using a grill.  For family gatherings, parties, tailgating, or a quiet night with loved ones, grilling can be a great way to prepare your meal.  I have always liked the flavor of food that was cooked on the grill, and as I have started to grill more and more I have been experimenting with new ways to use my grill.

<p>There are 4 distinct types of grills that you should consider when looking to find the right one for you:
<li><b>Charcoal</b>
     <ul>Cooking on a charcoal grill produces a more intense, smoked flavor. Cooking over charcoal requires a lot more time. Depending on grill size and the number and type of briquettes, you should be ready to cook 15 to 30 minutes after lighting. Sizes range from small models (great for tailgating or camping) to large grills.</ul>
<li><b>Propane</b>
     <ul>Propane gas burns cleaner and is less expensive per use than charcoal.  These grills ignite quickly and easily with a push-button, rotary or electronic lighter that's integrated into the grill. So, after only a few minutes of preheating, you'll be ready to cook.  They have a greater temperature control so you can cook food quicker and more evenly than using an open flame from charcoal.  Side burners on gas grills allow you to prepare an accompanying dish without running back and forth to the kitchen. A standard propane tank holds 20 pounds of fuel and depending on the cooking temperature and number of burners working, a full tank usually lasts around nine hours.</ul>
<li><b>Smoker</b>
     <ul>There are propane and charcoal based smokers, but the purpose of a smoker is a long, slow cooking of your meat.  There are many different sized smokers, with some of the largest allowing you to cook upwards of 50 pounds of meat at any one time.  Using a smoker, you are able to get a much more distinct flavor to your meat, enhanced by using different types of wood chips. </ul>
<li><b>Combination</b>
     <ul>A combination grill is what I ended up purchasing.  Having the flexibility to do any two or all three types of grilling above allows you to tailor your cooking to the situation at hand.  You get the ability to quickly heat the grill (and turn it off) if you are cooking for only one person while still able to cook on charcoal or with smoke for larger groups when I have more time. </ul>
</li>
<p>Be sure to check out a local store and the grills that they have in person to make sure that the size and quality of the grill are up to what you are looking for with your grilling needs.  I was able to see my exact model of the grill and another one that I was considering at <a href="http://www.lowes.com/Outdoors/Grills-Outdoor-Cooking/_/N-1z0wel9/pl?UserSearch=grills">Lowe's</a> before purchasing it, and it helped me to decide which one I really wanted to get!  

<p>Also, as the summer ends, a lot of places will have their grills and other outdoor equipment on sale.  Between September and November are a great time to pick up a discounted model that will be ready for you to use when Spring hits!

