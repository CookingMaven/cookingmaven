<p><b>Brown sugar:</b> 1 cup = 1 cup granulated sugar plus 2 Tbsp. molasses
<img src="images/cups.jpg" align="right" height="260" width="210"><p><b>Buttermilk:</b> 1 cup = 1 cup plain yogurt or 1 cup <a href="/glossary/37.php">sour milk</a>
<p><b>Cake flour, sifted:</b> 1 cup = 7/8 cup all purpose flour, sifted (1 cup minus 2 Tbsp.)
<p><b><a href="/glossary/23.php">Confectioners sugar:</a></b> 1 3/4 cup = 1 cup granulated sugar, packed
<p><b>Cornstarch </b>(for thickening): 1 tbsp.= 2 tbsp. flour
<p><b>Corn syrup:</b> 1 cup = 2/3 cup granulated sugar plus 1/3 cup water
<p><b>Cracker crumbs:</b> 3/4 cup = 1 cup bread crumbs
<p><b>Cream:</b> 1 cup = 1/3 cup butter plus 3/4 cup milk
<p><b>Eggs </b>(while baking): 1 egg = 1 tsp. cornstarch plus 1/4 cup water
<p><b>Flour </b>(for thickening): 1 tbsp. = 1 1/2 tsp. corn flour, arrowroot, potato flour, <b>or</b> rice flour; <b>or</b> 2 tsp. tapioca
<p><b>Fresh herbs:</b> 1 tbsp. = 1/3 to 1/2 tsp. dried herb (the same kind as fresh required)
<p><b>Garlic clove:</b> 1 = 1/8 tsp. garlic powder
<p><b>Margarine or butter </b>(while baking or cooking): 1 cup = 1 cup hard shortening <b>or</b> 7/8 cup vegetable oil
<p><b>Semi-sweet chocolate:</b> 1 ounce = 1 ounce unsweetened chocolate plus 4 tsp. sugar
<p><b>Sour cream </b>(while baking): 1 cup = 7/8 cup buttermilk or <a href="/glossary/37.php">sour milk</a> plus 3 Tbsp. of butter
<p><b>Sour cream </b>(in salad dressings, casseroles): 1 cup= 1 cup plain yogurt or 3/4 cup sour milk plus 1/3 cup butter
<p><b>Tapioca </b>(for thickening): 2 tbsp. = 3 Tbsp. flour
<p><b>Unsweetened chocolate:</b> 1 square = 3 tbsp. cocoa plus 1 Tbsp. oil
<p><b>Whole egg:</b> 1 egg = 2 egg yolks plus 1 Tbsp. water
<p><b>Whole milk:</b> 1 cup = 1/2 cup evaporated milk plus 1/2 cup water, <b>or</b> 1/3 cup dry milk plus 1 cup water


