<p>Recently, Maven <a href="/mavens/benjaminpower.php">Ben Power</a> and his friends, Maven Chris and Maven Scott, started a competition BBQ team, that competes in the KCBS BBQ circuit!  Here will be a review of competitions, tips on BBQ, and our favorite recipes highlighted from our experiences!  You can also follow along with us at <a href="http://www.instagram.com/thebbqmavens" target="new">@theBBQMavens</a> on Instagram!

