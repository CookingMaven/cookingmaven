<a name="whatis">
<h3><font color="#800000">What is CookingMaven.COM?</font></h3>
</a>
CookingMaven.COM is a website dedicated to the recipes of Ben's family members.  On this site there will be tips, help, and a respect of chefs (or cooks) in general.  There will be many unique features on this page to help you learn to cook, or improve your cooking skills. 
<a name="contacts">
<h3><font color="#800000">How can I contact the editor/webmaster?</font></h3>
</a>
You can contact Ben by e-mailing him at <A HREF="mailto:ben@cookingmaven.com">ben@cookingmaven.com</a> or on Twitter <a href="http://www.twitter.com/theCookingMaven">@theCookingMaven</a> where he will respond as soon as he reads the email.  

<a name = "submitting">
<h3><font color="#800000">Can I submit material to CookingMaven.COM?</font></h3></a>
Yes, just send your questions or tips to <A HREF="mailto:ben@cookingmaven.com">ben@cookingmaven.com</a> or on Twitter <a href="http://www.twitter.com/theCookingMaven">@theCookingMaven</a> for Ben to review it.

<a name = "accessprob">
<h3><font color="#800000">What if I have trouble accessing CookingMaven.COM web pages?</font></h3>
</a>
AQHost is providing hosting for CookingMaven.COM. If attempts to access the server result in a message that the server is unavailable, please send e-mail's to <A HREF="mailto:ben@cookingmaven.com">ben@cookingmaven.com</a> to notify the editor.

<a name = "support">
<h3><font color="#800000">How can I help support CookingMaven.COM?</font></h3>
</A>
As you probably saw, there is an "Ask the Maven!" page.  All of the questions that will be posted there are submitted to Ben.  To have your questions answered by the maven, just send an e-mail to <A HREF="mailto:ben@cookingmaven.com">ben@cookingmaven.com</a>.  Also, referring people to visit this site is a big help to the visibility of CookingMaven.COM.

