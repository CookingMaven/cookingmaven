<b>Prince's Hot Chicken</b> - Nashville, Tennessee

<p>When visiting Nashville, there are lots of options to try the local favorite, Hot Chicken.  In my recent stop in Nashville, I got to try Prince's Hot Chicken, who has been serving their Hot Chicken recipe for over 100 years.  With 2 locations in Nashville, they have perfected 6 levels of <a href="http://princeshotchicken.com/wp-content/uploads/2016/05/princes-hot-chicken-menu.jpg" target="new">spiciness</a> serving fried chicken and fried tenders with 6 options of sides.

<p>On my visit, I tried the breast quarter with wing in the medium spiciness and a side of beans.  Both were very tasty, and the chicken was also fairly spicy.  Be careful when choosing your spice levels as it can sneak up on you!  Prince's was a great way to enjoy the local specialty, and I would recommend you give it a try the next time you're in town!

<p>To find their locations, or to learn more about Prince's story, visit <a href="http://princeshotchicken.com/" target="new"> their website</a> or on Facebook at <a href="https://www.facebook.com/PrincesHotChicken/" target="new">Prince's Hot Chicken</a>.
