<b>Hershey's Chocolate World</b> - Hershey, PA

<p>During a trip to Pennsylvania for work, I was close to the Hershey Chocolate World.  I had a few free hours, and was able to stop by and check out what they had to offer!  From the <a href="http://www.hersheys.com/chocolateworld/chocolate-tour" target="new"> chocolate tour</a>, <a href="http://www.hersheys.com/chocolateworld/4D-Mystery" target="new">4D movie experience</a>, and <a href="http://www.hersheys.com/chocolateworld/chocolate-tasting" target="new">chocolate tasting lab</a> the activities are ones that the whole family can enjoy.  I got to try all 3 of these experiences out, and wish it was warmer so that I could have visted <a href="http://www.hersheypark.com/" target="new">Hershey Park</a> amusement park located next door to Chocolate World!

<p>The highlights of the visit for me were the chocolate tasting, the chocolate milkshake, and the free chocolate (see a theme here)!  The chocolate tasting lab allows guests to learn about the ways that Hershey's works to develop flavors, and best of all, sample 4 different kinds of Hershey's chocolate to experience the different flavors.  After finishing, guests receive a certificate signifying the completion and the guests new found title of Hershey's Palateer.
<p><center><img src = "/images/palateer.jpg" width="330" height="280"></center>

<p>The 4D movie, while tailored for younger children, used special effects to enhance the experience for all guests.  From the use of water sprays, streamers, 3D tricks, and vibrating seats all of the effects tied in nicely and made for a fun time.  Also, after the completion of the video each guest received a free small candy bar, as a nice little perk!

<p>To learn more about Chocolate World, visit <a href="http://www.hersheys.com/chocolateworld/" target="new"> their website</a> or follow them on twitter at <a href="https://twitter.com/chocolateworld" target="new">@ChocolateWorld</a> and be sure to stop by on your next trip through Southeastern Pennsylvania.
