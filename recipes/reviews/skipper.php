<b>Jungle Navigation Co. Ltd. Skipper Canteen</b> - Magic Kingdom, Walt Disney World: Orlando, FL

<p>I have been able to try a number of different Walt Disney World restaurants over my many trips to the Happiest Place on Earth, and a few have really stood out above the rest.  The Jungle Cruise is one of the most iconic attractions at the Magic Kingdom, and due to its popularity, Disney turned one of its restaurants into an experience based on this attraction.  The Jungle Navigation Co. Ltd. Skipper Canteen, located just a few steps away from the ride, serves cuisine from all over the world.  This is a far cry from your hamburger, chicken tenders, and french fries in a theme park!  I couldn't have been happier to see this type of restaurant come into the theme park, as I am a huge fan of trying new foods and sampling cuisine from around the world.

<p><img src = "/images/skippermenu.jpg" width="180" height="280" hspace="20" align="left">The menu is made up of unique chicken, beef, and seafood dishes that all have unique presentations and subtle puns to fit in with the theming of the Jungle Cruise.  I have tried a few of the meals including the Nile Nellie's Noodle Bowl (served with duck) and the "Taste's like Chicken" because it is!  I also was able to try two of the appetizers, including one of my favorite dishes in the theme park, the S.E.A. Shu Mai.  This dish is named after the Society of Explorers and Adventurers, and is "A legendary blend of Pork, Shrimp, Edamame Beans, and Spices wrapped in Gyoza Skin then steamed".  Be sure to order the S.E.A. Shu Mai if you are able to check this restaurant out on your next trip to Disney!

<p>The other part of the Skipper Canteen that must be mentioned is how the cast members are in character, as skippers, throughout your meal.  For those of you who have never ridden the Jungle Cruise before, these skippers are known for their "punny" humor and ability to crack jokes during tense situations on the river cruise.  Be sure to keep on your toes as they will crack jokes and have "terrible' humor throughout your meal!

<p>I have had two great experiences at this restaurant and highly recommend that you make a reservation for the Skipper Canteen on your next trip to Walt Disney World, as you won't be disappointed in the food or entertainment!

<p>To find out more about the Skipper Canteen, see the full menu, or plan your next trip to Walt Disney World, visit <a href="https://disneyworld.disney.go.com/dining/magic-kingdom/jungle-navigation-skipper-canteen//" target="new"> their website</a> or follow them on twitter at <a href="https://twitter.com/WDWToday" target="new">@WDWToday</a>.
