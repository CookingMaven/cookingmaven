<b>Willie Mae's Scotch House</b> - New Orleans, Louisiana

<p>When visiting New Orleans, everyone loves to try authentic southern fried chicken, and Willie Mae's Scotch House is one of the best!  Since 1957, Willie Mae's has been serving friend chicken and fried chicken tenders to hundreds of guests daily, including my family during our December 2016 trip to the Big Easy.  

<p>In order for us to try the "Best Chicken in America", as voted by the Travel Channel, we had to wait outside the restaurant for a few hours.  If you are planning on checking out this restaurant, be sure to leave plenty of time to get a table, and again to get your fresh cooked chicken.  In all, the lunch trip took us around 3 hours, but the chicken and sides were as great as advertised!

<p>I highly recommend trying Willie Mae's out on your next trip to New Orleans, and you won't be disappointed in the food!

<p>To find a location, or to learn more about Willie Mae's story, visit <a href="http://www.williemaesnola.com/" target="new"> their website</a> or follow them on twitter at <a href="https://twitter.com/WillieMaesNOLA" target="new">@WillieMaesNOLA</a> to learn more.
