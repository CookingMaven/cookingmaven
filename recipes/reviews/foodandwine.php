<b>EPCOT Food and Wine Festival</b> - Orlando, FL

<p><img src = "/images/foodandwinelogo.jpg" width="330" height="230" align="left" hspace="10"><p>In September of 2014, I was able to visit the Food and Wine Festival at EPCOT park in the Walt Disney World Resort.  This festival is a 3-month long chance to try unique food and drink options from around the world!  At EPCOT, you always have the opportunity to experience cuisine from 11 different countries (Mexico, Norway, China, Germany, Italy, USA, Japan, Morocco, France, UK, and Canada).  During the Food and Wine Festival, there are more than 30 different kiosks featuring cuisine from around the world; including Greece, Belgium, Brazil, Poland, and many, many, more!

<p>Each food and drink item is a "sample" size, allowing you to try multiple options as you make your way around the world!  A few of my favorites were the Chicken Gyro with Tzatziki Sauce from Greece, the Crispy Pork Belly with black beans and tomato from Brazil, and the Lamb Meatball with Spicy Tomato Chutney from New Zealand!  Really, you can't go wrong at any of the kiosks.  With options for vegetarians, vegans, or just picky eaters, you will be able to find something for everyone in your group!

<p>As I mentioned before, it wasn't just the food that makes Food and Wine so much fun, it's also the drinks!  Each kiosk features a few beer, wine, or other spirit options that are unique from the theme of that location.  Some of my personal favorites were the Local Beer selection at the Craft Beer station (featuring beers from the Orlando area), La Passion Martini Slush (Vodka, Grey Goose Le Citron Vodka, Cranberry and Passion Fruit Juice) from the France kiosk, and the Mezcal Pineapple Margarita on the Rocks from the Mexican kiosk.  

<p>I had a great time at the Food and Wine festival in 2014, and every year it continues to provide new food and drink options that are sure to be a hit with everyone, making it a must do for Disney fans and foodies alike!

<p><b>Pro Tip</b>: Try and avoid the weekends, if at all possible on your trip, as the crowds at EPCOT during the festival get to be very high and the lines at the kiosks can be very long.

<p>To learn more about the EPCOT Food and Wine Festival, visit <a href="https://www.epcotfoodfestival.com/ target="new"> their website</a> or follow Disney on twitter at <a href="https://twitter.com/WaltDisneyWorld" target="new">@WaltDisneyWorld</a>.
