<b>Joe's Kansas City Barbecue</b> - Kansas City, Kansas


<p>If you are looking for some of the best BBQ on the planet, then look no further than Joe's Kansas City!

<p>During my years in Kansas City, I have been able to be apart of one of the toughest debates around.  Similar to the best pizza place in New York City or the best cheesesteak in Philly, the best BBQ restaurant in Kansas City is highly contested.  For me, however, the clear winner every time is Joe's Kansas City Barbecue.  Formerly named Oklahoma Joe's, the only thing to change in the last 20 years has been the food, as they have continued to produce the best barbecue money can buy.

<p>Whether you are a fan of brisket, pork, chicken, or a combination of all 3, Joe's has you covered.  My personal favorite, and the favorites of many others, is the Z-Man sandwich (learn about the history of it <a href="http://www.joeskc.com/pages/the-legend-of-the-z-man">here</a>) or their rack of ribs.  I have yet to try a better rack of ribs or a better sandwich when it comes to BBQ.  Of course, no barbecue sandwich is complete without a side.  From baked beans to Kansas City's <a href="http://www.kansascity.com/living/liv-columns-blogs/chow-town/article141007518.html" target="new">best french fries</a>, Joe's has you covered for your whole meal!  Match the food with one of the most unique locations for a BBQ restaurant, a gas station, and you have one of the <a href="http://www.menshealth.com/nutrition/must-visit-restaurants">"13 Places to Eat Before you Die"!</a>

<p>To find one of their locations, or to learn more about the Joe's story, visit <a href="https://www.joeskc.com/" target="new"> their website</a> or follow them on twitter at <a href="https://twitter.com/joeskc" target="new">@JoesKC</a>.
