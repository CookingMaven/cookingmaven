<b>The American Royal BBQ Competition</b> - Kansas City, KS

<p><img src="../images/BBQMavens.jpg" hspace="20" height="250" width="315" align="left">The 2017 American Royal BBQ Competition was held the weekend of September 1st to 3rd at the Kansas Speedway in Kansas City, Kansas.  A group of friends and I decided to get a team together, <a href="/BBQMavens/index.php">The BBQ Mavens</a>, and compete in the Open Competition.  This was our first BBQ Competition, and was a lot of fun and a great experience!  The competition included Beef Brisket, Pork Butt, Pork Ribs, Chicken, Turkey, and Sausage turn ins, as well as optional side dishes and desserts for teams to compete in.  The turn-ins took place on Sunday starting at 11:30 am, which meant that we were up all night preparing and smoking the meats that we would turn in.

<p>During the competition we partnered with a friend of Maven Ben's who has created his own sauces under the name The Wolf Creek BBQ Co.  We also created our own <a href="../recipe/54.php">brine recipe</a> that we used on our chicken and turkey and our own <a href="../recipe/55.php">brisket rub</a> to use at the competition!  These are great for people to use at home when preparing their own BBQ!

<p>To find or to learn more about The American Royal and their BBQ Competition, visit <a href="http://www.americanroyal.com/bbq/" target="new"> their website</a> or follow them on twitter at <a href="https://twitter.com/AmericanRoyal" target="new">@AmericanRoyal</a>.


