<b>The Three Tuns</b> - London, England

<p>When wandering around London, you will inevitably walk past a number of pubs on every street and side street.  The choice can be overwhelming, and it is something that I struggled with on my first night in the city.  What I learned, though, is that most have similar beers and food choices, often being owned by specific breweries.  For example, The Three Tuns is owned by Greene King, and their beer is served here.

<p>I visited this pub on 3 different occasions over a week, and tried a number of their beers as well as their Fish and Chips dinner and their Wagyu Beef Burger.  Both were fantastic and cooked to perfection!  I saw a number of people eating their pies and other dishes, all that looked great as well.  The place is very stereotypically "British" in their design, food, and beer, and is exactly what I had hoped to find while in London!

